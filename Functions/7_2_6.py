"""
–°–æ–∑–¥–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–≤–Ω—É—é —Ñ—É–Ω–∫—Ü–∏—é, –æ–ø—Ä–µ–¥–µ–ª—è—é—â—É—é ùëÅ-—ã–π —á–ª–µ–Ω –≥–µ–æ–º–µ—Ç—Ä–∏—á–µ—Å–∫–æ–π –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏,
–µ—Å–ª–∏ –∏–∑–≤–µ—Å—Ç–µ–Ω –ø–µ—Ä–≤—ã–π —á–ª–µ–Ω –∏ –∑–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏.
"""

def geom_progr(n, denom, current_elem):
    if (n == 1):
        return current_elem
    return geom_progr(n - 1, denom, current_elem) * denom

n = int(input("–ù–æ–º–µ—Ä –Ω—É–∂–Ω–æ–≥–æ —á–ª–µ–Ω–∞ –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏: "))
denom = int(input("–ó–Ω–∞–º–µ–Ω–∞—Ç–µ–ª—å –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏: "))
first_elem = int(input("–ü–µ—Ä–≤—ã–π —á–ª–µ–Ω –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏: "))

print(f"{n} —á–ª–µ–Ω –ø—Ä–æ–≥—Ä–µ—Å—Å–∏–∏: {geom_progr(n, denom, first_elem)}")